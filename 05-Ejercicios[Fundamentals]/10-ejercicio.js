// Puedes usar este array para probar tu función:

const numbers = [12, 21, 38, 5, 45, 37, 6];


function average(param) {

  let total = 0;

  for (i = 0; i < param.length; i++) {
    let num = param[i];
    total = total + num;
  }

  let media = total / param.length;

  return media;

}



